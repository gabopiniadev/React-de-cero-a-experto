//Tipo de dato Const
//Es un tipo de dato estatico, el cual no puede ser modificado luego de ser usado.

console.log('===========================================')
console.log('Tipo Dato Const')
const nombre = 'Gabriel';
console.log(nombre);

//Tipo de dato Var
//Es un tipo de dato el cual puede ser modificado luego de ser usado.
//Esto es recomendable usar en EmmaScript 4/5
console.log('===========================================')
console.log('Tipo dato var')

var apellido = 'pena';
apellido = 'Pina';

console.log(apellido);


//Tipo de dato let
//Es un tipo de dato el cual puede ser modificado luego de ser usado.
console.log('===========================================')
console.log('Tipo dato let')

let noombre = 'Alejandro';
noombre= 'Belkys';

console.log(noombre);



